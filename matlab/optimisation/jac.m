function J = jac(X)

    % Assumption h(X) = [l1;l2;l3;P1;P3,z1,z2,z3,z4]
    % X = [x1 y1 x2 y2 x3 y3 x4 y 4]
    J = zeros(11,8);
    l1 = norm(X(1:2)-X(3:4));
    l2 = norm(X(3:4)-X(5:6));
    l3 = norm(X(5:6)-X(7:8));
    J(1,1) = (X(1)-X(3))/l1;
    J(1,2) = (X(2)-X(4))/l1;
    J(1,3) = -(X(1)-X(3))/l1;
    J(1,4) = -(X(2)-X(4))/l1;

    J(2,3) = (X(3)-X(5))/l2;
    J(2,4) = (X(4)-X(6))/l2;
    J(2,5) = -(X(3)-X(5))/l2;
    J(2,6) = -(X(4)-X(6))/l2;

    J(3,5) = (X(5)-X(7))/l3;
    J(3,6) = (X(6)-X(8))/l3;
    J(3,7) = -(X(5)-X(7))/l3;
    J(3,8) = -(X(6)-X(8))/l3;

    J(4,1) = 1;
    J(5,2) = 1;
    J(6,7) = 1;
    J(7,8) = 1;

    J(8,2) = 1;
    J(9,4) = 1;
    J(10,6) = 1;
    J(11,8) = 1;
